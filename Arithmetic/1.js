//Arithmetic Operators

//Addition
let z = x + y; //z is 15

//Subtracting
let a = x - y; //a is 5

//Multiplication
let p = x * y; //p is 50

//Dividing
let q = x / y; //q is 2

//Remainder
let r = x % y; //r is 0

//Incrementing
let s = ++x; //s is 11

//Decrementing
let t = --y; //t is 4

//Exponentiation
let u = x ** y; //u is 100000
