//JavaScript Operators

//Assignment Operators

let x = 10;
let y = 5;

x += y  // x = x + y; //x is now 15
x -= y  // x = x - y; //x is now 10
x *=y   // x = x * y; //x is now 50
x /= y  // x = x / y; //x is now 10
x %= y  // x = x % y; //x is now 0
x **= y // x = x ** y; //x is now 100000

