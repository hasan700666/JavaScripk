//The if Statement and The else Statement

if (20>2){
    console.log("20 is a big number");
}else{
    console.log("2 is a big number🤣🤣");
}

//The else if Statement

if (20>2){
    console.log("20 is a big number");
}else if (15>2){
    console.log("15 is a big number");
}else{
    console.log("2 is a big number🤣🤣");
}

