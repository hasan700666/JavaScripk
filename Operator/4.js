//Bitwise Operators

//AND
let c = 5; // 0101
let d = 3; // 0011
console.log(c & d); // 1

//OR
console.log(c | d); // 7

//XOR
console.log(c ^ d); // 6

//NOT
console.log(~c); // -6

//Left Shift
console.log(c << 1); // 10

//Right Shift
console.log(c >> 1); // 2

//Unsigned Right Shift
console.log(c >>> 1); // 2