//Logical Operators

//AND
let a = true;
let b = false;

if(a && b){
    console.log("Both are true");
}
else{
    console.log("At least one is false");
}

//OR
if(a || b){
    console.log("At least one is true");
}
else{
    console.log("Both are false");
}

//NOT
if(!a){
    console.log("a is false");
}
else{
    console.log("a is true");
}
