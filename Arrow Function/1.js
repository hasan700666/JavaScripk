//Arrow functions 
//Arrow functions allow us to write shorter function

//ex
//Before Arrow:

hello1 = function(){
    return "hello World"
};

//With Arrow Function:

hello2 = () => {
    return "Hello World"
};

//Arrow Functions Return Value by Default:

hello3 = () => {"Hello World"};

//Arrow Function With Parameters:

hello4 = (value) => {"Hello World" + value};

//Arrow Function Without Parentheses:

hello5 = value => {"Hello World" + value};