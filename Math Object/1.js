//Number to Integer
//There are 4 common methods to round a number to an integer:

//1. Math.round()

console.log(Math.round(4.5));
console.log(Math.round(46.9));  //Math.round(x) returns the nearest integer
console.log(Math.round(3.2));

//2. Math.ceil()

console.log(Math.ceil(4.2));
console.log(Math.ceil(5.1));  //Math.ceil(x) returns the value of x rounded up to its nearest integer
console.log(Math.ceil(9.9));

//3. Math.floor()

console.log(Math.floor(4.7));
console.log(Math.floor(5.1));  //Math.floor(x) returns the value of x rounded down to its nearest integer
console.log(Math.floor(9.9));

//4. Math.trunc()

console.log(Math.trunc(4.4));
console.log(Math.trunc(5.9));  //Math.trunc(x) returns the integer part of x:
console.log(Math.trunc(9.1));

//Math.sign()

console.log(Math.sign(-4));
console.log(Math.sign(4));
console.log(Math.sign(0));  //Math.sign(x) returns if x is negative, null or positive:
console.log(Math.sign(-1));

//Math.pow()

console.log(Math.pow(8,2));  //it minse 8^2
console.log(Math.pow(9,10));  //it minse 9^10 //Math.pow(x, y) returns the value of x to the power of y
console.log(Math.pow(20,1));  //it minse 20^1

//Math.sqrt()

console.log(Math.sqrt(64)); //it mines 8^2 = 64
console.log(Math.sqrt(25));  //it mines 5^2 = 25  //Math.sqrt(x) returns the square root of x:
console.log(Math.sqrt(100));  //it mines 10^2 = 100

//Math.abs()

console.log(Math.abs(-4.5));
console.log(Math.abs(-10.5));  //Math.abs(x) returns the absolute (positive) value of x:
console.log(Math.abs(4.5));

//Math.sin()

console.log(Math.sin(1));
console.log(Math.sin(0));
console.log(Math.sin(90));

//Math.cos()

console.log(Math.cos(1));
console.log(Math.cos(0));
console.log(Math.cos(90));

//Math.min() and Math.max()

console.log(Math.min(0, 150, 30, 20, -8, -200));  //find the small number

console.log(Math.max(0, 150, 30, 20, -8, -200));  //find the big number

//Math.random()

console.log(Math.random());  //Math.random() returns a random number between 0 (inclusive), and 1 (exclusive)

